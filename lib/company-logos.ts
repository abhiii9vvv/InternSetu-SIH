// Function to get logo path for a company name
export function getCompanyLogo(companyName: string): string | null {
  const normalizedName = companyName.toUpperCase().trim()
  
  // Direct match
  if (companyLogos[normalizedName]) {
    return companyLogos[normalizedName]
  }
  
  // Partial match - check if any key contains the company name or vice versa
  for (const [key, logo] of Object.entries(companyLogos)) {
    if (key.includes(normalizedName) || normalizedName.includes(key)) {
      return logo
    }
  }
  
  return null
}

// Function to check if a company has a logo available
export function hasCompanyLogo(companyName: string): boolean {
  return getCompanyLogo(companyName) !== null
}